interface MenuItem {
  href: string;
  label: string;
  icon: React.FC<{ className?: string }>;
}

interface SidebarProps {
  userName?: string;
  onLogout?: () => void;
}

export const Sidebar: React.FC<SidebarProps> = ({ 
  userName = 'Usuario', 
  onLogout 
}) => {
  const pathname = usePathname();
  
  const menuItems: MenuItem[] = [
    { href: "/dashboard", label: "Dashboard", icon: HomeIcon },
    { href: "/profile", label: "Perfil", icon: UserIcon },
    { href: "/history", label: "Historial", icon: ClockIcon },
    { href: "/settings", label: "Ajustes", icon: GearIcon },
  ];

  const isActive = (path: string) => {
    return pathname === path || (path === '/dashboard' && pathname === '/');
  };

  const handleLogout = () => {
    if (onLogout) {
      onLogout();
    }
  };

  return (
    <aside className={styles.sidebar}>
      {/* Logo */}
      <div className={styles.logo}>
        <div className={styles.logoIcon}>
          <SparkIcon className="h-6 w-6 text-white" />
        </div>
        <div className={styles.logoText}>
          <div className={styles.logoTitle}>Ahorro</div>
          <div className={styles.logoSubtitle}>INVISIBLE</div>
        </div>
      </div>
      
      {/* Menu */}
      <div className={styles.menu}>
        <div className={styles.menuHeader}>
          MENU PRINCIPAL
        </div>
        
        <nav className={styles.menuList}>
          {menuItems.map((item) => {
            const active = isActive(item.href);
            
            return (
              <Link 
                key={item.href}
                href={item.href} 
                className={`${styles.menuItem} ${active ? styles.menuItemActive : ''}`}
              >
                <span className={`${styles.menuIcon} ${active ? styles.menuIconActive : ''}`}>
                  <item.icon className="h-5 w-5" />
                </span>

                <span className={styles.menuLabel}>{item.label}</span>

                {active && (
                  <span className={styles.menuChevron}>
                    <ChevronRightIcon className="h-4 w-4" />
                  </span>
                )}
              </Link>
            );
          })}
        </nav>
      </div>
      
      {/* Theme Toggle */}
      <div className="px-4 mt-auto mb-4">
        <ThemeToggle />
      </div>
      
      {/* User info - bottom */}
      <div className={styles.user}>
        <div className={styles.userInfo}>
          <div className={styles.userAvatar}>
            {userName.charAt(0).toUpperCase()}
          </div>
          <div className={styles.userName}>{userName}</div>
        </div>
        
        <button 
          onClick={handleLogout}
          className={styles.logoutButton}
        >
          <LogoutIcon className="h-5 w-5" />
          Cerrar Sesi√≥n
        </button>
      </div>
    </aside>
  );
};

export default Sidebar;
